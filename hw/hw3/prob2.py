def main():
    x = 3.14
    print("x equals " + str(x))
    x = 2
    print("x equals " + str(x))
    x = "hello world"
    print("x equals " + str(x))

if __name__ == "__main__":
    main()

# Python is dynamically typed so the values carry the type.
# Therefore x has the type of whatever is currently in it.
# The real type lies with 3.4 or 5 or “hello”.
